class calculator:
    def addition(x,y):
        add=x+y
        print(add)

    def subtract(x,y):
        sub=x-y
        print(sub)

    def multiply(x,y):
        mul=x*y
        print(mul)

    def division(x,y):
        div=x/y
        print(div)

calculator.addition(10,20)
calculator.subtract(40,30)
calculator.multiply(30,40)
calculator.subtract(400,20)
